void main(){

for(int i=0; i<10; i++)
{
  if(i%2==0){
    print("$i is Even");
  }
  else{
    print("$i is old");
  }
}
int j=10;
print("\n  end of for");
while(j>0){
  if(j%2==0){
    print("$j is Even");
  }
  else{
    print("$j is old");
  }
  j--;
}

// do while
print("\n this is do while loop");
var x=0;
do{
print(x);
x++;
}while(x<10);
}